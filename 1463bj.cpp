#include <iostream>

using namespace std;
int n;
int d[1000005]; //N에 해당하는 10^6 이하 정수에 대해 "연산 횟수"에 관한 테이블 정의

int main() {

	ios::sync_with_stdio(0);
	cin.tie(0);

	cin >> n;
	
	d[1] = 0; //연산횟수 초기값 [1을 1로 만들기 위한 연산횟수 0회]

	//테이블 채우기 d[12]=? d[1]~d[11]까지 테이블 채워놔서 파악가능
	for (int i = 2; i <= n; i++) {
		d[i] = d[i - 1] + 1;
		if (i % 3 == 0) d[i] = min(d[i],d[i / 3] + 1);
		if (i % 2 == 0) d[i] = min(d[i],d[i / 2] + 1);
	}
	cout << d[n];

	return 0;
}